# You should write a program to search a binary matrix (a pattern)
# within another binary matrix (an image). The recognition process
# consists of scanning the image matrix row by row (horizontal scanning)
# and when a pattern is located on the image, the program must mark this
# pattern. To mark a located pattern change 1 to 3 and 0 to 2. The
# result will be the image matrix with the located patterns marked.
# The patterns in the image matrix are not crossed, because you should
# immediately mark the pattern.
# Input: Two arguments. A pattern as a list of lists and an image as a
# list of lists.
# Output: The marked image as a matrix as a list of list.

def checkio(pattern, image):
    i_height = len(image)
    i_width = len(image[0])
    p_height = len(pattern)
    p_width = len(pattern[0])
    for i in range(i_height-p_height+1):
        for j in range(i_width-p_width+1):
            if all(image[i+n][j+m] == pattern[n][m] for n in range(p_height) for m in range(p_width)):
                for n in range(p_height):
                    for m in range(p_width):
                        image[i+n][j+m] += 2
    return image

